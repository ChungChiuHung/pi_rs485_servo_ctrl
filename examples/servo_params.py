    #   原始狀態  : 0F 00 00 00
    #   設定值    : 60 00 00 01
    #   遮罩      : 00 00 00 01
    #   寫入後狀態: 0F 00 00 01

    # Response: E I/O Section
    # 處理結果 2 bytes
    #    - 0x00 正常
    #    - 0x01 異常
